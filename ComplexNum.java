class ComplexNum {
    public double x; //Действительная часть (коэфф a)
    public double y; //Мнимая часть (коэфф b)

    ComplexNum(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX(){
        return x;
    }

    public double getY(){
        return y;
    }
}
